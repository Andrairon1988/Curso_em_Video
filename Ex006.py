n = int(input('Digite um número: '))
dobro = n * 2
triplo = n * 3
raizquadrada = n ** (1/2)
print('O dobro de {} vale {}'.format(n, dobro))
print('O triplo de {} vale {}'.format(n, triplo))
print('O raiz quadrada de {} vale {:.2f}'.format(n, raizquadrada))

# O comando \n quebra a linha.
# O comando :.2f formata o número em decimal com duas casas.